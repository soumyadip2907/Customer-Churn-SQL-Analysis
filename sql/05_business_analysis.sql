-- Q50: Generate a customer summary report using a CTE and CASE.
WITH customer_summary AS (
		SELECT CASE WHEN tenure < 12 THEN '0-11 Months'
					WHEN tenure BETWEEN 12 AND 23 THEN '12-23 Months'
					WHEN tenure BETWEEN 24 AND 47 THEN '24-47 Months'
					WHEN tenure BETWEEN 48 AND 71 THEN '48-71 Months'
					ELSE '72+ Months' END AS tenure_group, churn,
					COUNT(*) AS total_customers,
					ROUND(AVG(monthly_charges), 2) AS average_monthly_charges,
					ROUND(AVG(total_charges), 2) AS average_lifetime_charges
		FROM customers
		GROUP BY
		CASE 	WHEN tenure < 12 THEN '0-11 Months'
				WHEN tenure BETWEEN 12 AND 23 THEN '12-23 Months'
				WHEN tenure BETWEEN 24 AND 47 THEN '24-47 Months'
				WHEN tenure BETWEEN 48 AND 71 THEN '48-71 Months'
				ELSE '72+ Months' END, churn)
SELECT * FROM customer_summary
ORDER BY tenure_group, churn;