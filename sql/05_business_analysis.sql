-- Q45: Rank contract types by churn rate using RANK().
WITH contract_churn AS (
		SELECT contract, COUNT(*) AS total_customers,
		SUM(CASE WHEN churn = 'Yes' THEN 1 ELSE 0 END) AS churned_customers,
		ROUND(100.0 * SUM(CASE WHEN churn = 'Yes' THEN 1 ELSE 0 END) / COUNT(*),2)
		AS churn_rate_percentage
    FROM customers
    GROUP BY contract)
SELECT contract, total_customers, churned_customers, churn_rate_percentage,
	RANK() OVER (ORDER BY churn_rate_percentage DESC) AS churn_rank
FROM contract_churn;

-- Q46: Rank payment methods by average monthly charges using DENSE_RANK().
SELECT payment_method, ROUND(AVG(monthly_charges), 2) AS average_monthly_charges,
		DENSE_RANK() OVER (ORDER BY AVG(monthly_charges) DESC) AS charge_rank
FROM customers
GROUP BY payment_method;

-- Q47: Divide customers into monthly charge quartiles using NTILE().

/* Use NTILE() to segment customers into quartiles, making it
easier to compare customer groups based on their monthly
spending patterns. */

SELECT customer_id, monthly_charges,
      NTILE(4) OVER (ORDER BY monthly_charges) AS charge_quartile
FROM customers
ORDER BY monthly_charges;

-- Q48: Identify the top 10 customers by lifetime revenue using ROW_NUMBER().
SELECT customer_id, total_charges,
		ROW_NUMBER() OVER (ORDER BY total_charges DESC) AS revenue_rank
FROM customers
WHERE total_charges IS NOT NULL
ORDER BY total_charges DESC
LIMIT 10;

-- Q49: Identify customer segments with the highest churn rate using a CTE.
WITH contract_churn AS (
		SELECT contract, COUNT(*) AS total_customers,
		SUM(CASE WHEN churn = 'Yes' THEN 1 ELSE 0 END) AS churned_customers,
		ROUND(100.0 * SUM(CASE WHEN churn = 'Yes' THEN 1 ELSE 0 END) / COUNT(*),2)
		AS churn_rate_percentage
	FROM customers
    GROUP BY contract)
SELECT contract, total_customers, churned_customers, churn_rate_percentage
FROM contract_churn
